<p>I am sure my physics professor would be amused with my using the atomic diagram to illustrate an important aspect of racquetball.&nbsp; The return, is without question, the nucleus of playing racquetball, that defining moment during the rally where a player scores a point or wins the serve for an opportunity to score a point.&nbsp; While the return may not rise to an atomic level, the process of returning the ball back and forth between players has certainly produced some of the most electrifying and memorable rallies of the sport.&nbsp;</p>

<p>This article reviews <b>Rule 3.14(c) Responsibility</b> of the <a target="new" href="https://www.teamusa.org/USA-Racquetball/rules/3-Play-Regulations">USAR Official Rules & Regulations</a> and addresses the following four distinct concepts of the return:</p>

<ol>
	<li>The offensive player's three entitled elements of attempting the return</li>
	<li>Responsibilities of the defensive player</li>
	<li>Responsibilities of the offensive player</li>
	<li>Criterion to assess a hinder</li>
</ol>

<p>Take a moment to review Rule 3.14(c)</p>

<div class="alert alert-warning">
	<h4><i class="fa fa-book"></i> Rule 3.14(c) Responsibility</h4>
	While making an attempt to return the ball, a player is entitled to a fair chance to see and return the ball. It is the responsibility of the side that has just hit the ball to move so the receiving side may go straight to the ball and have an unobstructed view of and swing at the ball. However, the receiver is responsible for making a reasonable effort to move towards the ball and must have a reasonable chance to return the ball for any type of hinder to be called.
</div>

<div class="alert alert-info">
	<h4><i class="fa fa-info-circle"></i> Concept 1: </h4>
	While making an attempt to return the ball, the offensive player is entitled to see the ball, have a straight path to the ball, and room to swing at and hit the ball.
</div>

<p><b>Discussion:</b>  When it’s the offensive player's turn to hit the ball, the player has first priority on the court, and as such, can for the most part, move anywhere on the court of his/her choosing and attempt to return any shot selection the player desires.  Rule 3.14(c) entitles the offensive player to three key elements of executing a return: <em>View, Path and Swing!</em></p>

<p>The offensive player’s “fair chance” is articulated through these three elements of attempting the return. </p>

<p>It’s also important to note that the ball is entitled to a clear path, and once struck, the ball is entitled to an uninterrupted path to its desired location.</p>

<div class="alert alert-info">
	<h4><i class="fa fa-info-circle"></i> Concept 2:  </h4>
	It is the defensive player's responsibility to move sufficiently to afford the offensive player the three entitled elements of executing the return.
</div>

<p><b>Discussion:</b>  The defensive player has second priority, and must move sufficiently so all three entitled elements of executing the return (View, Path, Swing!) are afforded to the offensive player, while maintaining position to return the pending shot.  This second priority is a fascinating concept of racquetball. Perhaps a discussion for a future article. </p>

<p>Once again, the defensive player must move sufficiently irrespective of the shot selection the offensive player chooses to make during the attempted return e.g., ceiling, back wall, side wall, straight to the front wall, down the line, cross court far corner. When the defensive player does not move to afford the offensive player these three elements of the return, it could result in a replay hinder, a penalty hinder, or no hinder at all.</p>  

<div class="alert alert-info">
	<h4><i class="fa fa-info-circle"></i> Concept 3:  </h4>
	The offensive player is responsible for making a reasonable effort to move towards the ball. 
</div>

<p><b>Discussion:</b> Here, the focus shifts from the entitled aspects of the return provided by the defensive player to the physical aspects of returning the ball by the offensive player. </p>

<p>Two key actions from the offensive player provide indications the player is making a reasonable effort to move towards the ball: 1)  when the player physically moves towards the ball, and 2) the offensive player’s racquet preparation to hit the ball.  Keep in mind that sometimes a hinder, such as a screen, can be so extreme that it takes away the offensive player's ability to make a reasonable chance to move towards the ball.</p>

<div class="alert alert-info">
	<h4><i class="fa fa-info-circle"></i> Concept 4:  </h4>
	There must be a reasonable chance the ball would legally make it to the front wall before the referee can assess a replay hinder or a penalty hinder.
</div>

<p><b>Discussion:</b> Concept 4 defines the criterion for assessing a penalty hinder or a replay hinder.
</p>

<div class="alert alert-danger">
	<h4><i class="fa fa fa-exclamation-circle"></i> What is a hinder?</h4>
	 While not specifically defined in the rules, based on my experience I would offer this definition:  A hinder is an occurrence during the rally that prevents the offensive player from making an attempt to return the ball.
<br/><br/>
<b>Note:</b> it’s not any occurrence, but an occurrence.  Those occurrences that qualify as hinders are identified in Rules 3.13 and 3.14. It’s not possible to identify every possible situation beyond a player's control, possibly occuring during the rally and potentially qualifying as a hinder.  Referees must evaluate the situation, and sometimes in the interest of fairness, it’s often best to simply replay the rally, or in extreme cases, assess a loss of rally.
</div>

<p>
Well, back to the concept.

It’s not enough to get to the ball and hit the ball.  There must be a reasonable chance the offensive player’s attempt to return the ball would result in a legally returned ball that touches the front wall before touching the floor (see Rule 3.11(c)). 

